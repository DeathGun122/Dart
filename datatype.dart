void main() {
  // Strings
  String name = "Rohan";
  print("Name is $name");

  // Numbers
  int age = 20;
  double height = 5.10;
  print("Age is $age and height is $height");

  // Boolean
  bool isAdult = true;
  print("Is Adult $isAdult");

  // Dynamic
  dynamic name1 = "Rohan";
  print("Name is $name1");

  // Constants
  const String name2 = "Rohan";
  final String name3 = "Rohan";
  print("Name is $name2 and $name3");
}