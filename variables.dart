void main() {
  print(19);
  // No keywords, numbers in names

  // Any
  var name = "John";
  // String
  String name1 = "John";
  // Integer
  int x = 441;
  print(x);
  print(name1);
  print(name);

  // Dynamic
  dynamic firstName = "Rohan";
  print(firstName);

  // const and final
  const String fullname = "John Doe";
  final String name2 = "John Doe";
  print(fullname);
  print(name2);

  // Not assigning
  var myname;
  print(myname);
}
